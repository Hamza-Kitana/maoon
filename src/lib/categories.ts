/** Serializable category shape stored in the CMS store (icons referenced by name). */
export type CategoryData = {
  slug: string;
  title: string;
  short: string;
  desc: string;
  iconName: string;
  color: string; // tailwind gradient stops
  /** empty string = use the bundled default image for this slug */
  image: string;
  longDesc: string;
  whoWeHelp: string[];
  howItWorks: string[];
  impact: string;
};

/** Resolved category used by UI components. */
export type Category = Omit<CategoryData, "iconName"> & { icon: LucideIcon };

/** Icons available for the admin to pick from. */
export const CATEGORY_ICONS: Record<string, LucideIcon> = {
  scale: Scale,
  home: Home,
  "graduation-cap": GraduationCap,
  "book-open": BookOpen,
  droplets: Droplets,
  zap: Zap,
  "heart-handshake": HeartHandshake,
  accessibility: Accessibility,
  stethoscope: Stethoscope,
  flame: Flame,
  heart: Heart,
  baby: Baby,
  gift: Gift,
  "shield-check": ShieldCheck,
  utensils: Utensils,
  "hand-helping": HandHelping,
  wallet: Wallet,
  truck: Truck,
};

export const DEFAULT_CATEGORY_IMAGES: Record<string, string> = {
  gharemoon: imgGharemoon,
  rent: imgRent,
  "univ-held": imgUnivHeld,
  students: imgStudents,
  water: imgWater,
  electricity: imgElectricity,
  elderly: imgElderly,
  disability: imgDisability,
  medical: imgMedical,
  disasters: imgDisasters,
};

export function resolveCategory(c: CategoryData): Category {
  const { iconName, ...rest } = c;
  return {
    ...rest,
    icon: CATEGORY_ICONS[iconName] ?? HeartHandshake,
    image: c.image || DEFAULT_CATEGORY_IMAGES[c.slug] || imgGharemoon,
  };
}

